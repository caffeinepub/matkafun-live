// Helper to convert HH:MM to bigint unix timestamp (using today's date)
function timeToTimestamp(hhmm: string): bigint {
  const [h, m] = hhmm.split(":").map(Number);
  const d = new Date();
  d.setHours(h, m, 0, 0);
  return BigInt(Math.floor(d.getTime() / 1000));
}

